let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

//Template Literals
let epicPhrase2 = `${hello} ${world}!`
console.log(epicPhrase2);

//Multi-line Strings
let lorem = 'Esto es un string\n' + 'Esto es otra línea';

let lorem2 = `Esto es un string
Esto es otra línea`;

console.log(lorem);
console.log(lorem2);
